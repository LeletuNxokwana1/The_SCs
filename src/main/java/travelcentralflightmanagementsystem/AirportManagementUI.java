    private void refreshAirportTable() {

        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();

        // Clear existing data and column headings in the table
        tableModel.setRowCount(0);
        tableModel.setColumnIdentifiers(new Object[]{"Airport Name", "IATA Code", "City Name"});

        // Populate the combo box with city names
        List<String> cityNames = programManager.retrieveCityNames();
        DefaultComboBoxModel<String> cityComboBoxModel = new DefaultComboBoxModel<>(cityNames.toArray(new String[0]));
        cmbCity.setModel(cityComboBoxModel);

        // Retrieve airport data from the database and update the table
        List<Airport> airports = programManager.retrieveAirports();

        // Populate the table with the retrieved airports
        for (Airport airport : airports) {
            // Get city name by city ID
            String cityName = programManager.getCityNameByID(airport.getCityID());

            // Add a row to the table model with airport details
            tableModel.addRow(new Object[]{
                airport.getAirportName(),
                airport.getAirportIATACode(),
                cityName // Display city name in the table
            });
        }
    }

