    private class StatusCellEditor extends AbstractCellEditor implements TableCellEditor {

        private JComboBox<String> comboBox;

        public StatusCellEditor(String[] items) {
            this.comboBox = new JComboBox<>(items);
        }

        @Override
        public Object getCellEditorValue() {
            return comboBox.getSelectedItem();
        }

        @Override
        public Component getTableCellEditorComponent(JTable table, Object value, boolean isSelected, int row, int column) {
            comboBox.setSelectedItem(value);
            return comboBox;
        }

        @Override
        public boolean isCellEditable(EventObject e) {
            // Make the cell editable for all events
            return true;
        }
    }

    private UserStatus convertToUserStatusEnum(String status) {
        switch (status) {
            case "Active":
                return UserStatus.ACTIVE;
            case "Deactivated":
                return UserStatus.DEACTIVATED;
            default:
                // Handle unexpected cases, maybe throw an exception or return a default value
                return UserStatus.ACTIVE;
        }
    }

