    private void countUserBookingsAndUpdateStatus(int selectedRow, String selectedStatus) {
        
        int userID = (int) jTable1.getValueAt(selectedRow, 0);
        
        UserStatus updatedStatus = convertToUserStatusEnum(selectedStatus);
        programManager.updateUserStatus(userID, updatedStatus);

        // Refresh the table after updating status for all rows
        displayUserInformation();
    }

    private void displayUserInformation() {
        List<User> users = programManager.retrieveUsersInformation();

        DefaultTableModel model = new DefaultTableModel() {
            @Override
            public Class<?> getColumnClass(int columnIndex) {
                return columnIndex == 6 ? JComboBox.class : Object.class;
            }

            @Override
            public boolean isCellEditable(int row, int column) {
                return column == 6 && super.isCellEditable(row, column);
            }
        };

        model.addColumn("User ID");
        model.addColumn("First Name");
        model.addColumn("Last Name");
        model.addColumn("Email Address");
        model.addColumn("Phone Number");
        model.addColumn("Password");
        model.addColumn("Status");
        model.addColumn("Retrieval Question");
        model.addColumn("Retrieval Answer");
        model.addColumn("Bookings Count");

        String[] statusItems = {"Active", "Deactivated"};

        for (User user : users) {
            model.addRow(new Object[]{
                user.getUserID(),
                user.getFirstName(),
                user.getLastName(),
                user.getEmailAddress(),
                user.getPhoneNumber(),
                user.getPassword(),
                user.getUserStatus(),
                user.getRetrievalQuestion(),
                user.getRetrievalAnswer(),
                programManager.countUserBookings(user.getUserID())
            });
        }

        jTable1.setModel(model);

        // Set the Status column to use the StatusCellEditor
        int statusColumnIndex = model.findColumn("Status");
        JComboBox<String> statusComboBox = new JComboBox<>(statusItems);
        statusComboBox.setSelectedItem("Active"); // Set default status if needed
        jTable1.getColumnModel().getColumn(statusColumnIndex).setCellEditor(new DefaultCellEditor(statusComboBox));
    }

