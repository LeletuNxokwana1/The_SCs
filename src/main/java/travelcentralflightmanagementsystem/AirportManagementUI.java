    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        // Get values from the input fields
        String airportName = txtfieldAirportName.getText().trim();
        String iataCode = txtfieldIATACode.getText().trim();
        String selectedCity = (String) cmbCity.getSelectedItem();

        // Check if all relevant information is inserted
        if (!airportName.isEmpty() && !iataCode.isEmpty() && selectedCity != null) {

            // Insert the airport into the database
            programManager.addAirport(airportName, iataCode, selectedCity);

            // Refresh the table after adding the airport
            refreshAirportTable();
        } else {
            // Handle the case where information is missing
            JOptionPane.showMessageDialog(this, "Please fill in all the fields", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnAddActionPerformed

